public class Ball {

    String form = "Round";
    int size = 5;
    String color = "White";

    public Ball (String form, int size, String color){
        this.form = form;
        this.size = size;
        this.color = color;
    }

    public String getForm(){
        return getForm();
    }

    public int getSize(){
        return getSize();
    }

    public String getColor(){
        return getColor();
    }
}
